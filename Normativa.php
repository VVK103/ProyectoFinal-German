<h1> Reglas Viales </h1>

<p> reglas del reglamento vial de Uriangato

<li>1. Las disposiciones del reglamento son de orden público y obligatorias para todas las personas en el municipio.</li>


<li>2. El objetivo del reglamento es preservar la vida, salud y patrimonio de las personas regulando el tránsito vehicular y peatonal en vías públicas municipales.</li>


<li>3. Peatones tienen preferencia de paso: los conductores deben ceder el paso a peatones siempre, respetando zonas peatonales, pasos peatonales y señales. 
Orden Jurídico</li>

<li>4. Las banquetas sólo pueden usarse para tránsito peatonal — no pueden ser ocupadas por vehículos, bicicletas o motocicletas, salvo autorización especial. 
Orden Jurídico</li>

<li>5. Motociclistas y ciclistas deben circular por el extremo derecho de la vía; nunca en paralelo entre ellos. 
Orden Jurídico</li>

<li>6. Si hay ciclopista disponible, ciclistas deben usarla — no pueden circular fuera de ella de forma indiferenciada. 
Orden Jurídico</li>

<li>7. Está prohibido que motocicletas o bicicletas se sujeten a otro vehículo en circulación, por ejemplo “ ir atados” a un automóvil. 
Orden Jurídico</li>

<li>8. Motociclistas/motonetistas no pueden circular con exceso de ruido ni emisión excesiva de humo. 
Orden Jurídico</li>

<li>9. El reglamento no aplica en vías de jurisdicción estatal o federal: solo rige dentro del municipio, en calles o caminos de jurisdicción municipal.</li>

<li>10. Todo usuario de vía pública (peatón, conductor, ciclista, pasajero) está obligado a respetar las señales, indicaciones de agentes de tránsito, y las disposiciones del reglamento.</li>
</p>
